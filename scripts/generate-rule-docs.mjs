import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, relative, resolve } from "node:path";
import process from "node:process";

import { parseRuleCatalogYaml } from "../packages/core/dist/src/index.js";
import { format } from "prettier";

const root = process.cwd();
const catalogPath = resolve(root, "specs/RULE_CATALOG.yaml");
const rulesIndexPath = resolve(root, "docs/rules/README.md");
const checkMode = process.argv.includes("--check");

const catalog = parseRuleCatalogYaml(await readFile(catalogPath, "utf8"));
const categories = new Map(
  catalog.categories.map((category) => [category.id, category])
);

const outputs = [
  {
    path: rulesIndexPath,
    content: await formatMarkdown(renderIndex())
  },
  ...(await Promise.all(
    catalog.rules.map(async (rule) => ({
      path: resolve(root, rule.documentation),
      content: await formatMarkdown(renderRule(rule))
    }))
  ))
];

const stale = [];

for (const output of outputs) {
  if (checkMode) {
    const existing = await readExisting(output.path);
    if (existing !== output.content) {
      stale.push(relative(root, output.path));
    }
    continue;
  }

  await mkdir(dirname(output.path), { recursive: true });
  await writeFile(output.path, output.content);
}

if (checkMode && stale.length > 0) {
  throw new Error(
    `Rule documentation is stale or missing:\n${stale
      .map((path) => `- ${path}`)
      .join("\n")}`
  );
}

if (checkMode) {
  console.log(`verified ${outputs.length} generated rule documentation files`);
} else {
  console.log(`generated ${outputs.length} rule documentation files`);
}

async function readExisting(path) {
  try {
    return await readFile(path, "utf8");
  } catch (error) {
    if (error && error.code === "ENOENT") {
      return undefined;
    }
    throw error;
  }
}

function renderIndex() {
  const lines = [
    "# SearchLint Rule Documentation",
    "",
    "<!-- Generated by scripts/generate-rule-docs.mjs. Do not edit by hand. -->",
    "",
    `Catalog version: ${catalog.version}`,
    `Status: ${catalog.status}`,
    `Rules: ${catalog.rules.length} / ${catalog.targetRuleCount}`,
    "",
    "## Categories",
    ""
  ];

  for (const category of catalog.categories) {
    const rules = catalog.rules.filter((rule) => rule.category === category.id);
    lines.push(`### ${category.title}`);
    lines.push("");
    lines.push(`Rules: ${rules.length} / ${category.targetCount}`);
    lines.push("");
    lines.push("| Rule | Name | Severity | Confidence | Sources |");
    lines.push("| ---- | ---- | -------- | ---------- | ------- |");
    for (const rule of rules) {
      lines.push(
        `| [${rule.id}](./${rule.id}.md) | ${rule.name} | ${rule.defaultSeverity} | ${rule.confidence} | ${rule.sources.join(", ")} |`
      );
    }
    lines.push("");
  }

  return `${lines.join("\n").trimEnd()}\n`;
}

async function formatMarkdown(content) {
  return format(content, { parser: "markdown", proseWrap: "always" });
}

function renderRule(rule) {
  const category = categories.get(rule.category);
  const providerNotes = implementationNotes(rule);
  const lines = [
    `# ${rule.id}: ${rule.name}`,
    "",
    "<!-- Generated by scripts/generate-rule-docs.mjs. Do not edit by hand. -->",
    "",
    "## Summary",
    "",
    rule.description,
    "",
    "## Metadata",
    "",
    `- Category: ${category?.title ?? rule.category} (${rule.category})`,
    `- Default severity: ${rule.defaultSeverity}`,
    `- Confidence: ${rule.confidence}`,
    `- Scope: ${rule.scope}`,
    `- Provider scope: ${rule.providerScope}`,
    `- Version: ${rule.version}`,
    "",
    "## Evidence Sources",
    "",
    ...rule.sources.map((source) => `- ${source}`),
    "",
    "## Required Evidence",
    "",
    ...rule.requiredEvidence.map((evidence) => `- ${evidence}`),
    "",
    "## When This Triggers",
    "",
    rule.checkingAlgorithm,
    "",
    "## False Positives",
    "",
    falsePositiveGuidance(rule),
    "",
    "## How To Fix",
    "",
    rule.fix,
    "",
    "## Examples",
    "",
    "Bad-case fixture reference:",
    "",
    ...rule.testExamples.map((example) => `- ${example}`),
    "",
    "Fixed direction:",
    "",
    `- ${rule.fix}`,
    "",
    "The generated documentation does not invent HTML, DSL, source locations, selectors, or external observations beyond the catalog fixture references above.",
    "",
    "## Implementation Surfaces",
    "",
    ...providerNotes.map((note) => `- ${note}`),
    "",
    "## Links",
    "",
    `- Catalog source: ../../specs/RULE_CATALOG.yaml`,
    `- Documentation path: ${rule.documentation}`
  ];

  return `${lines.join("\n").trimEnd()}\n`;
}

function falsePositiveGuidance(rule) {
  if (rule.confidence === "heuristic") {
    return "This rule is heuristic. Treat the diagnostic as a review prompt and use DSL severity overrides or suppressions when project policy intentionally differs.";
  }

  if (rule.providerScope === "google" || rule.providerScope === "yandex") {
    return "False positives are usually caused by stale or sampled external observations. Check the observation date, freshness, and provider payload before suppressing.";
  }

  return "False positives are expected to be rare because this rule relies on deterministic local evidence. Suppress only with a reason when the route contract intentionally differs from the observed signal.";
}

function implementationNotes(rule) {
  const notes = [];
  if (rule.sources.includes("source-code")) {
    notes.push(
      "Next.js/source analyzer can provide source-code evidence when the relevant files are supplied."
    );
  }
  if (
    rule.sources.includes("raw-html") ||
    rule.sources.includes("rendered-dom") ||
    rule.sources.includes("http-header")
  ) {
    notes.push(
      "CLI, crawler, browser collector, overlay, and Next.js dev mode can share this rule through the common core engine."
    );
  }
  if (
    rule.sources.includes("crawler") ||
    rule.sources.includes("robots-txt") ||
    rule.sources.includes("sitemap")
  ) {
    notes.push(
      "Crawler snapshots can provide crawl, robots.txt, sitemap, and site-graph evidence."
    );
  }
  if (rule.providerScope === "google" || rule.sources.includes("google")) {
    notes.push(
      "Google integration must provide dated, freshness-aware observations before this rule can use Google evidence."
    );
  }
  if (rule.providerScope === "yandex" || rule.sources.includes("yandex")) {
    notes.push(
      "Yandex integration must provide dated, freshness-aware observations before this rule can use Yandex evidence."
    );
  }
  if (notes.length === 0) {
    notes.push(
      "This rule is executed through the shared deterministic core engine."
    );
  }
  return notes;
}
